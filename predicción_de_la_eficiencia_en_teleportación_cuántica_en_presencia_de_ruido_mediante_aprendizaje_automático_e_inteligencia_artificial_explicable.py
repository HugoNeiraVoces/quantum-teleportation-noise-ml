def get_bloch_vector_from_rho(rho_data):
    sx = np.array([[0, 1], [1, 0]])
    sy = np.array([[0, -1j], [1j, 0]])
    sz = np.array([[1, 0], [0, -1]])
    x = np.real(np.trace(rho_data @ sx))
    y = np.real(np.trace(rho_data @ sy))
    z = np.real(np.trace(rho_data @ sz))
    return [x, y, z]

