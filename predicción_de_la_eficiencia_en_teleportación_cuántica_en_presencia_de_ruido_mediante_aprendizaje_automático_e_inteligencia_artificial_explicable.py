def crear_circuito_teletransporte():
    theta = np.random.uniform(0.0, 1.0) * np.pi
    varphi = np.random.uniform(0.0, 2.0) * np.pi

    qc = QuantumCircuit(3, 3)

    #Estado de Alice
    qc.u(theta, varphi, 0.0, 0)
    qc.barrier()

    #Entrelazamiento
    qc.h(1)
    qc.cx(1, 2)
    qc.barrier()

    #Protocolo Alice
    qc.cx(0, 1)
    qc.h(0)
    qc.barrier()

    #Medidas de Alice
    qc.measure(0, 0)
    qc.measure(1, 1)

    #Correcciones de Bob
    with qc.if_test((0, 1)):
        qc.z(2)
    with qc.if_test((1, 1)):
        qc.x(2)

    qc.barrier()

    #Inversa para medir fidelidad
    qc.u(theta, varphi, 0.0, 2).inverse()

    #Probabilidad (fidelidad real)
    qc.save_probabilities([2])

    #Fidelidad Medida estándar
    qc.measure(2, 2)

    return qc

