    qc.h(1)
    qc.cx(1, 2)
    qc.barrier()

        #Alice's protocol
    qc.cx(0, 1)
    qc.h(0)
    qc.barrier()

        #Alice's measurements
    qc.measure(0, 0)
    qc.measure(1, 1)

        #Bob's corrections
    with qc.if_test((0, 1)):
        qc.z(2)
    with qc.if_test((1, 1)):
        qc.x(2)

    qc.barrier()

        #Inverse for fidelity measurement
    qc.u(theta, varphi, 0.0, 2).inverse()

        #Probability (real fidelity) ---
    qc.save_probabilities([2])

    qc.save_density_matrix([2], label='rho_bob')

        #Measured Fidelity
    qc.measure(2, 2)

    return qc

