from scipy.stats import norm

def calcular_metricas_finales(mu_ia, sigma_ia, mu_hw, sigma_hw):
        # Z-score
    z = np.abs(mu_ia - mu_hw) / np.sqrt((sigma_ia)**2 + (sigma_hw)**2)
    return z

